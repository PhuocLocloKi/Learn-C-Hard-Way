#include <stdio.h> // Standard Input Output library

/**
 * SECTION 1: C PROGRAM STRUCTURE
 * - #include: Preprocessor command to include libraries.
 * - int main(): The entry point where execution begins.
 * - printf(): Function to output text to the screen.
 * - return 0: Ends the program and returns status to the OS.
 */

int main() {
    // Basic Output
    printf("Hello World!\n"); // \n is a Newline character

    // Escape Sequences
    printf("1. Backslash: \\\n");
    printf("2. Tab space: \t[Tabbed]\n");
    printf("3. Double quote: \"Hello\"\n");
    printf("4. Question mark: \?\n");

    /* Multi-line comment:
        This code helps you understand the basics.
    */
    return 0;
}